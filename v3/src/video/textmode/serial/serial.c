#include "serial.h"

/* Intialize USB, wait for host. We will _BLOCK_ (!!!) at usb_configured(), 
 * if there was no connected USB host. We will block at host_is_ready()
 * if the USB host isn't ready to recieve (aka doesn't have a terminal emulator running) 
 */
void initialize_serial(){
	usb_init();
	while (!usb_configured()) /* wait */ ;
	_delay_ms(1000);
	block_on_host();
	usb_serial_flush_input();
}

uint8_t host_is_ready(){
	return (usb_serial_get_control() & USB_SERIAL_DTR);
}

void block_on_host(){
	while (!host_is_ready()) /* wait */ ;
}

// Send a string to the USB serial port.  The string must be in
// flash memory, using PSTR
//
void send_str(const char *s)
{
	char c;
	while (1) {
		c = pgm_read_byte(s++);
		if (!c) break;
		usb_serial_putchar(c);
	}
}

// Receive a string from the USB serial port.  The string is stored
// in the buffer and this function will not exceed the buffer size.
// A carriage return or newline completes the string, and is not
// stored into the buffer.
// The return value is the number of characters received, or 255 if
// the virtual serial connection was closed while waiting.
//
uint8_t recv_str(char *buf, uint8_t size)
{
	int16_t r;
	uint8_t count=0;

	while (count < size) {
		r = usb_serial_getchar();
		if (r != -1) {
			if (r == '\r' || r == '\n') return count;
			if (r >= ' ' && r <= '~') {
				*buf++ = r;
				usb_serial_putchar(r);
				count++;
			}
		} else {
			if (!usb_configured() ||
			  !(usb_serial_get_control() & USB_SERIAL_DTR)) {
				// user no longer connected
				return 255;
			}
			// just a normal timeout, keep waiting
		}
	}
	return count;
}


